    private void cargarDatosDeVenta() {

        DAOVenta dao = new DAOVentaImpl();
        try {
            // --- 1. CARGAR DATOS GENERALES ---
            Object[] datosVenta = dao.obtenerVentaConCliente(this.idVenta);
            if (datosVenta != null) {
                txtCodigoVenta.setText(String.valueOf(datosVenta[0]));
                txtFecha.setText(String.valueOf(datosVenta[1]));
                txtCliente.setText((String) datosVenta[2]);
                txtDNI.setText((String) datosVenta[3]);
                // El campo txtDNI1 parece un duplicado, puedes usarlo para el total o eliminarlo.
                // Aquí lo usaré para el total.
                txtTotal.setText(String.format("S/ %.2f", datosVenta[4]));

                // Hacemos que los campos no sean editables
                txtCodigoVenta.setEditable(false);
                txtFecha.setEditable(false);
                txtCliente.setEditable(false);
                txtDNI.setEditable(false);
                txtTotal.setEditable(false);
            }

            // --- 2. CARGAR TABLA DE PRODUCTOS ---
            DefaultTableModel modeloTabla = new DefaultTableModel() {
                @Override
                public boolean isCellEditable(int row, int colum) {
                    return false;
                }
            };
            String titulos[] = {"Cod. Prod.", "Producto", "Cantidad", "Precio Unit.", "Sub-total"};
            modeloTabla.setColumnIdentifiers(titulos);

            List<Object[]> listaDetalles = dao.listarDetallesPorVenta(this.idVenta);
            for (Object[] detalle : listaDetalles) {
                // Formateamos los precios
                detalle[3] = String.format("%.2f", detalle[3]);
                detalle[4] = String.format("%.2f", detalle[4]);
                modeloTabla.addRow(detalle);
            }
            TbProductosComprados.setModel(modeloTabla);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar los detalles: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }

    }

