    private void CBeditProvActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CBeditProvActionPerformed

        try {
            List<Proveedor> listaProveedores = dao.listarProveedores();
            String nombreSeleccionado = (String) CBeditProv.getSelectedItem();

            for (Proveedor p : listaProveedores) {
                if (p.getEmpresa().equals(nombreSeleccionado)) {
                    txtxEditIDProveedor.setText(String.valueOf(p.getId_proveedor()));
                    break;
                }
            }
        } catch (Exception e) {
            e.getMessage();
        }
    }//GEN-LAST:event_CBeditProvActionPerformed


