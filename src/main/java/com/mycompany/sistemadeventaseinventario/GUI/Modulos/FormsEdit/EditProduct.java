    private void mostrarProveedoresEnCB() {
        try {
            List<String> proveedoresName = dao.obtenerNombresEmpresas();

            CBeditProv.removeAllItems();

            for (String nombre : proveedoresName) {

                CBeditProv.addItem(nombre);
            }

        } catch (Exception e) {
        }

    }


    private void btnGuardarEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarEditActionPerformed

        String nombre, marca, proveedor, observaciones;
        int id_proveedor, stock;
        double precio;
        Proveedor prov = new Proveedor();

        if (txtEditName.getText().isEmpty() || txtEditMarca.getText().isEmpty() || txtEditPrecio.getText().isEmpty() || txtxEditStock.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe completar los campos", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }

        nombre = txtEditName.getText();
        marca = txtEditMarca.getText();
        proveedor = CBeditProv.getSelectedItem().toString();
        observaciones = txeditObs.getText();
        id_proveedor = Integer.parseInt(txtxEditIDProveedor.getText());
        stock = Integer.parseInt(txtxEditStock.getText());
        precio = Double.parseDouble(txtEditPrecio.getText());

        try {

            DAOProveedores daoprov = new DAOProveedoresImpl();
            prov = daoprov.traerProveedor(id_proveedor);

            Producto NuevoProd = new Producto();

            NuevoProd.setNombre(nombre);
            NuevoProd.setMarca(marca);
            NuevoProd.setStock(stock);
            NuevoProd.setPrecio(precio);
            NuevoProd.setObservacion(observaciones);
            NuevoProd.setProovedor(prov);

            DAOProducto daoProd = new DAOProductoImpl();
            daoProd.Editar(Id_producto, NuevoProd, prov);
            JOptionPane.showMessageDialog(this, "Se actualizarón los datos del producto", "DATOS ACTUALIZADOS", JOptionPane.INFORMATION_MESSAGE);
            ventanaInventario.CargarTablaInventario();
            this.dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ocurrió un error al actualizar: " + e.getMessage(), "Error de Base de Datos", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnGuardarEditActionPerformed

