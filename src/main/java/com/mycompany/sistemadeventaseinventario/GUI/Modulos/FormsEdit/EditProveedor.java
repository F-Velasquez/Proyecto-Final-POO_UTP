    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        String Empresa, Contacto, Telefono, Email, ProdSuministrados, Direccion;

        if (txtEmpresa.getText().isEmpty() || txtContacto.getText().isEmpty() || txtTelefono.getText().isEmpty() || txtEmail.getText().isEmpty() || txtProductosSum.getText().isEmpty() || txtDireccion.getText().isEmpty()) {

            JOptionPane.showMessageDialog(this, "Debe completar todos los campos", "", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Empresa = txtEmpresa.getText();
        Contacto = txtContacto.getText();
        Telefono = txtTelefono.getText();
        Email = txtEmail.getText();
        ProdSuministrados = txtProductosSum.getText();
        Direccion = txtDireccion.getText();

        try {

            Proveedor prov = new Proveedor();
            prov.setEmpresa(Empresa);
            prov.setContacto(Contacto);
            prov.setTelefono(Telefono);
            prov.setEmail(Email);
            prov.setProdSuministrados(ProdSuministrados);
            prov.setDireccion(Direccion);

            daoP.Editar(prov, idProveedor);
            JOptionPane.showMessageDialog(this, "Ciente modificado", "", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
            VentanaProveedores.cargarTabla();

        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

